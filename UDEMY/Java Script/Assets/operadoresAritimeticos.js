/*Operadores:*/

//Adição (+) - Soma valores
/*Por esse sinal de mais servir também para uma concatenação, caso
o valor seja do tipo string a operação sera de concatenação. 
Por isso é importante ficar atento ao contexto de uso do operador +*/

// Subtração (-) - Diferença entre valores

// Multiplicação (*) - Produto dos valores

// Divisão (/) - Quociente dos valores

// Módulo (%) - Resto existente em uma operação de divisão
/*O modulo é o resto de uma divisão, por exemplo: se dividirmos
7 por 2, sobraria 1, esse 1 seria o resultado do Módulo.
DETALHE: O módulo sempre retornara valores inteiros.*/

// Incremento (++) - Pré/pós incremento
/*O incremento é quando temos determinado valor e vamos adicionar apenas
uma unidade a este valor, por exemplo: eu tenho um valor 10 e faço uma 
operação de incremento, esse meu valor 10 passara a ser 11 a mesma coisa
para o operador de decremento.  */

// Decremento (--) - Pré/pós decremento
/*Seguindo o exemplo acima, digamos que eu tenha um valor 10
e eu queira fazer o decremento de uma unidade nele, ele ira passar
a ser 9.*/

/** Operadores de Decremento e Incremento podem ser pre ou pos atribuidos.
A posição do operador indicara se ele é pre ou pos. 
Ou seja se a operação de incremento ou decremento vier a esquerda
do valor, signfica que ele é pre, isso quer dizer que o java script
ira dar preferencia para a execução da operação de incremento ou
decremento antes de utilizar este valor ou variavel na operação seguinte.
E caso ele venha apos o valor ou a variavel, a operação de incremento ou decremento
seria pos.**/





